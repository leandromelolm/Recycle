package com.pdm.recycle.model;

public class Residuo {
    private int codResiduo;
    private String nome;
    private String descricao;
    private String imagemResiduo;

    public int getCodResiduo() {
        return codResiduo;
    }

    public void setCodResiduo(int codResiduo) {
        this.codResiduo = codResiduo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getImagemResiduo() {
        return imagemResiduo;
    }

    public void setImagemResiduo(String imagemResiduo) {
        this.imagemResiduo = imagemResiduo;
    }
}
